package com.LinkedList;
import java.util.LinkedList;
public class BuiltinLinkedList {
    public static void main(String[] args) {


        LinkedList<String> list=new LinkedList<>();
        list.add("45");
        list.add("55");
        list.add("65");
        list.add("75");
        System.out.println(list);

        list.addFirst("1");
        System.out.println(list);

        list.addLast("100");
        System.out.println(list);















    }
















}
